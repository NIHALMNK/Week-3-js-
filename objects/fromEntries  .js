// Object.fromEntries 
//Convert the array [ ['name', 'Alice'], ['age', 25] ] into an object using Object.fromEntries().
let person=[ ['name', 'Alice'], ['age', 25] ]

const data=Object.fromEntries(person)
console.log(data);//=>{ name: 'Alice', age: 25 }
